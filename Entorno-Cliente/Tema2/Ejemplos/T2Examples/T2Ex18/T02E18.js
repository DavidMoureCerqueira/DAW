// Constantes máis habituais
console.log("Constantes:");
console.log("Math.E: "+  Math.E);            // Número de Euler
console.log("Math.PI: "+  Math.PI);          // Pi
console.log("Math.SQRT2: "+  Math.SQRT2);    // Raíz cadrada de 2
console.log("Math.LN2: "+  Math.LN2);        // Logaritmo natural de 2

// Métodos para redondeo
console.log("Redondeo:");
console.log("Math.round(4.3): "+  Math.round(4.3));     // 4
console.log("Math.round(4.5): "+  Math.round(4.5));     // 5
console.log("Math.ceil(4.1): "+  Math.ceil(4.1));       // 5
console.log("Math.floor(4.9): "+  Math.floor(4.9));     // 4
console.log("Math.trunc(4.9): "+  Math.trunc(4.9));     // 4 (elimina decimais)

// Funcións numéricas
console.log("Funcións numéricas:");
console.log("Math.pow(2, 3): "+  Math.pow(2, 3));       // 8 (2 elevado a 3)
console.log("Math.sqrt(16): "+  Math.sqrt(16));         // 4
console.log("Math.abs(-5): "+  Math.abs(-5));           // 5
console.log("Math.sign(-5): "+  Math.sign(-5));         // -1
console.log("Math.sign(0): "+  Math.sign(0));           // 0

// Xeración de números aleatorios
console.log("Aleatorios:");
console.log("Math.random(): "+  Math.random());                         // Entre 0 (inclusivo) e 1 (exclusivo)
console.log("Número entre 1 e 10: "+  Math.floor(Math.random() * 10) + 1); // Entre 1 e 10              OJO EXAMEN

// Máximos e mínimos
console.log("Máximos e mínimos:");
console.log("Math.max(1, 5, 3): "+  Math.max(1, 5, 3));   // 5
console.log("Math.min(1, 5, 3): "+  Math.min(1, 5, 3));   // 1

// Outros métodos útiles
console.log("Outros métodos:");
console.log("Math.log(10): "+  Math.log(10));             // Logaritmo natural de 10
console.log("Math.log10(100): "+  Math.log10(100));       // Logaritmo base 10
console.log("Math.sin(Math.PI / 2): "+  Math.sin(Math.PI / 2)); // 1 (seno de 90 graos)