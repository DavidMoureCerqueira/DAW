def comproba_par(numero):
    """
    Funcion que indica se un numero e par ou impar
    
    Args:
        n(int): un enteiro o cal se vai comprobar se e par
    
    Returns: 
        (boolean) True se e par ou False se e impar

    """
    if numero % 2 == 0:
        return True
    else:
        return False


