def mayor(num1, num2):
    if num1>num2:
        print(f"{num1} es mayor que {num2}")
    elif num1<num2:
        print(f"{num2} es mayor que {num1}")
    else:
        print("Es el mismo numero")



def menor(num1, num2):
    if num1<num2:
        print(f"{num1} es menor que {num2}")
    elif num1>num2:
        print(f"{num2} es menor que {num1}")
    else:
        print("Es el mismo numero")
        