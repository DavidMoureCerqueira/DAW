"""Exercicio 4. Escribe un script que pida ao usuario dous números enteiros e realiza a división enteira do primeiro número entre o segundo. Mostra o resultado por pantalla."""

numero1 = int(input("Introduzca el dividendo\n"))
numero2 = int(input("Introduzca el divisor\n"))
division=numero1//numero2
print(f"El resultado de dividir {numero1} entre {numero2} es: {division}")